    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Logics logic = new Logics();
        Persion persion = new Persion();
        persion.setName(jTextField1.getText());
        persion.setId(jTextField2.getText());
        persion.setSex(logic.getSexFromId(jTextField2.getText()));
        persion.setAddress(jTextField4.getText());
        persion.setTpnum(jTextField5.getText());
	persion.setBirthday(jTextField2.getText());
        
        DBCon db = new DBCon();
        try {
            db.addPersionToDatabase(persion);
            JOptionPane.showMessageDialog(this, "Added");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

